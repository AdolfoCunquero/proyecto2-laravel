    public function signup(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'name' => 'required',
            'email' => 'required|email',
            'password' => 'required',
            'confirm_password' => 'required|same:password',
        ]);
   
        if($validator->fails()){
            return $this->sendError('Error validation', $validator->errors());       
        }
   
        $input = $request->all();
        $input['password'] = bcrypt($input['password']);
        $user = User::create($input);
        $success['token'] =  $user->createToken('proyecto2')->accessToken;
        $success['name'] =  $user->name;
   
        return $success;//$this->sendResponse($success, 'User created successfully.');
    }

    public function signin(Request $request)
    {
        if(Auth::attempt(['email' => $request->email, 'password' => $request->password])){ 
            $authUser = Auth::user(); 
            $success['token'] =  $authUser->createToken('proyecto2')->accessToken; 
            $success['name'] =  $authUser->name;
   
            return $success;//$this->sendResponse($success, 'User signed in');
        } 
